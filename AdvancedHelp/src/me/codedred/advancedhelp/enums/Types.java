	public static final Types[] VALUES = Types.values();
    private final byte data;
    private final String[] legacy;
	
    
    Types(int data, String... legacy) {
        this.data = (byte) data;
        this.legacy = legacy;
    }
    
    
    /**
     * Checks if Types contains the materials name
     *
     * @param name - name of the material
     * @return true - if Types enum has this material.
     */
    public static boolean contains(String name) {
        String formatted = format(name);
        return Arrays.stream(Types.VALUES).anyMatch(mat -> mat.name().equals(formatted));
    }
    

    /**
     * Matches the argument string and its data with a XMaterial.<br>
     *
     * @param name the name of the material
     * @param data the data byte of the material
     * @return a XMaterial from the enum (with the same legacy name and data if in older versions.)
     */
    @SuppressWarnings("unused")
    public static ItemStack matchMaterial(String name) {
        name = format(name);
       
        List<String> values = new ArrayList<String>();
        String newName = null;
        ItemStack item;
        if (Types.valueOf(name).getLegacy().length <= 0)
        	return parseItem(name,(byte) -1);
        for (String oldName : Types.valueOf(name).getLegacy()) {
        	try {
        		values.add(oldName);
        		item = new ItemStack(Material.matchMaterial(oldName));
        	}catch (Exception e) {
        		values.remove(oldName);
        	}
        }
        newName = values.get(0);
        if (newName == null || values.isEmpty()) 
        	return defaultItem();
        
        byte d = Types.valueOf(name).getData();
        
        try {
        	return parseItem(newName,d);
        } catch (Exception e) {
        	return defaultItem();
        }
    }
    

    
    /**
     * Get a list of materials names that was previously used by older versions.
     *
     * @return a list of string of legacy material names.
     */
    public String[] getLegacy() {
        return this.legacy;
    }
    
    /**
     * Get the {@link ItemStack} data of this material in older versions.
     *
     * @return data of this material.
     */

    public byte getData() {
        return this.data;
    }

    
    /**
     * Attempts to build the string like an enum name.
     *
     * @param name the material name to modify.
     * @return a Material enum name.
     */
    private static String format(String name) {
        return StringUtils.replace(StringUtils.replace(name.toUpperCase(), "MINECRAFT:", ""), "-", "_").trim()
                .replaceAll("\\s+", "_").replaceAll("\\d+", "").replaceAll("\\W+", "");
    }
    
    
    /**
     * Parses the Type as an {@link ItemStack}.
     *
     * @param Types name, ID data if exists
     * @return an ItemStack with the same material (and data if in older versions.)
     */
    @SuppressWarnings("deprecation")
	public static ItemStack parseItem(String name, byte data) {
        Material material = Material.matchMaterial(name);
        if (data != -1)
        	return new ItemStack(material, 1, data);
        return new ItemStack(material);
    }

    /**
     * Gets an itemstack of material paper
     *
     * @return paper as a default value if the item cannot be found.
     */
    public static ItemStack defaultItem() {
        ItemStack item = new ItemStack(Material.matchMaterial("PAPER"));
        return item;
    }

